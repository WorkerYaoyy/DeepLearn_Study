# 导入PyTorch深度学习框架
import torch

# 创建一维张量：数值0~11，数据类型为32位浮点数
# 再重塑形状为3行4列的二维矩阵（3×4张量），赋值给x
X = torch.arange(12, dtype=torch.float32).reshape((3, 4))
# 手动创建3行4列浮点张量y，内部填充自定义数值
Y= torch.tensor([[2.0, 1, 4, 3],
                  [1, 2, 3, 4],
                  [4, 3, 2, 1]])
# torch.cat：张量拼接函数
# dim=0 代表按行拼接（纵向堆叠），把y放到x下方
# 输入元组(x,y)表示要拼接的两个张量，拼接后形状为(6,4)
print(torch.cat((X, Y), dim=0))
# dim=1 代表按列拼接（横向拼接），把y拼在x右侧
# 拼接后每行元素合并，最终形状为(3,8)
print(torch.cat((X, Y), dim=1))


#id
# ====================== 第一块：普通运算会开辟新内存，变量地址改变 ======================
# 获取张量Y当前的内存地址（id()查看对象内存标识）
before = id(Y)
# 张量加法运算，Y + X 会生成全新张量，再赋值给变量Y
# 旧Y的内存不会原地修改，变量Y会指向新内存地址
Y = Y + X
# 判断修改前后Y的内存地址是否相同，输出False，证明分配了新内存
print(id(Y) == before)
# ====================== 第二块：原地赋值操作，不改变张量内存地址 ======================
# 创建和Y形状、数据类型完全一致、全为0的张量Z
Z = torch.zeros_like(Y)
# 打印初始状态下张量Z的内存地址
print('id(Z):', id(Z))
# 原地赋值：把X+Y的计算结果填充进Z的原有存储空间，不新建张量对象
# Z[:] 代表对Z全部元素原地覆盖，变量Z本身指向的内存地址不变
Z[:] = X + Y
# 再次打印Z的内存地址，和上面打印结果完全一致
print('id(Z):', id(Z))
